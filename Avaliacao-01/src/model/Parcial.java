package model;

public class Parcial {
	
	private int idJurado;
	private int idEscola;
	private int idQuesito;
	private double nota;
	
	
	public int getIdJurado() {
		return idJurado;
	}
	public void setIdJurado(int idJurado) {
		this.idJurado = idJurado;
	}
	public int getIdEscola() {
		return idEscola;
	}
	public void setIdEscola(int idEscola) {
		this.idEscola = idEscola;
	}
	public int getIdQuesito() {
		return idQuesito;
	}
	public void setIdQuesito(int idQuesito) {
		this.idQuesito = idQuesito;
	}
	public double getNota() {
		return nota;
	}
	public void setNota(double nota) {
		this.nota = nota;
	}
}

